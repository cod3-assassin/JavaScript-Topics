let x = Math.floor(Math.random() * 100 + 1);
let y = Math.floor(Math.random() * 50 + 1);

//Get the Sum
const sum = x + y;
const sumOutput = `${x} + ${y} = ${sum}`;

console.log(sumOutput);

//Get the Diff
const diff = x - y;
const diffOutput = `${x} - ${y} = ${diff}`;

console.log(diffOutput);

//Get the Mulitplication

const mult = x * y;
const multOutput = `${x} * ${y} = ${mult}`;

console.log(multOutput);

//Get Divide

const divid = x / y;
const dividOutput = `${x} / ${y} = ${divid}`;

console.log(dividOutput);

//Get the Modules

const modules = x % y;
const modulesOutput = `${x} % ${y} = ${modules}`;

console.log(modulesOutput);
