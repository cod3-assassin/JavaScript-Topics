let x;
console.log(x);

//SquireRoot
x = Math.sqrt(9);
console.log(x);

//Absolute
x = Math.abs(-5);
console.log(x);

//Round

x = Math.round(4.6);
console.log(x);

//Round UP

x = Math.ceil(4.2);
console.log(x);

//Round DOWN

x = Math.floor(4.9);
console.log(x);

//Power

x = Math.pow(2, 3);
console.log(x);

//MIN & MAX

x = Math.min(4, 5, 2);
console.log(x);

x = Math.max(2, 5, 2);
console.log(x);

//Random

x = Math.random();
console.log(x);

x = Math.floor(Math.random() * 10 + 1);
console.log(x);
